import numpy as np

# 2d array
a = np.array([[1,2,3],[4,5,6]])
print(a)

# shape of the numpy array
print('a.shape :', a.shape)

# size -> returns the total number of elements in the array
print('a.size :', a.size)

# dtype -> gives the data type of elements in the array
print('a.dtype :', a.dtype)

# ndim -> returns the dimension of the array
print('a.ndim :', a.ndim)

# nbytes -> returns to size of array in bytes
print('a.nbytes :', a.nbytes)